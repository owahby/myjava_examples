public class PE0518B
{
  public static void main(String[] args)
  {
    int n = 6;

    for(int i=1; i<=n; n--)
    {
      for(int j=1; j<=n; j++)
      {
          System.out.print(j + " ");
      }
      System.out.println();
    }

  }
}
